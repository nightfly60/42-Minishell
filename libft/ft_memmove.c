#include "libft.h"

void	*ft_memmove(void *dest, const void *src, unsigned long n)
{
	if (!dest && !src)
		return (0);
	if (dest == src)
		return (dest);
	if (src > dest)
		ft_memcpy(dest, src, n);
	else
	{
		while (n > 0)
		{
			n--;
			((char *)dest)[n] = ((char *)src)[n];
		}
	}
	return (dest);
}
